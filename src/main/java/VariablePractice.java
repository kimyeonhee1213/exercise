public class VariablePractice {
    public static void main(String[] args) {
        int price;
        price = 1300;
        int num = 6;
        int totalPrice = price * num;
        System.out.println("오렌지 한 개 가격: " + price + "원");
        System.out.println("오렌지 갯수: " + num + "개");
        System.out.println("오렌지 총 가격: " + totalPrice + "원");
    }
}